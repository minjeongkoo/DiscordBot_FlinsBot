// 유저별 대화 히스토리 (최근 10턴)
const histories = new Map();

function getHistory(userId) {
    if (!histories.has(userId)) histories.set(userId, []);
    return histories.get(userId);
}

function resetHistory(userId) {
    histories.set(userId, []);
}

function buildPrompt(userId, userMessage) {
    const history = getHistory(userId).slice(-10);

    const historyText = history
        .map((t) => `사용자: ${t.user}\n플린스: ${t.assistant}`)
        .join("\n");

    return `${SYSTEM_PROMPT}\n\n${historyText}\n사용자: ${userMessage}\n플린스:`;
}

function saveHistory(userId, userMessage, assistantReply) {
    const history = getHistory(userId);
    history.push({ user: userMessage, assistant: assistantReply });
}

module.exports = { buildPrompt, saveHistory, resetHistory };